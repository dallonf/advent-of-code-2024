from textwrap import dedent
import aoc2024.common.input as aoc_input
from .day16 import Maze


def test_maze_1():
    map = aoc_input.lines(
        dedent(
            """
            ###############
            #.......#....E#
            #.#.###.#.###.#
            #.....#.#...#.#
            #.###.#####.#.#
            #.#.#.......#.#
            #.#.#####.###.#
            #...........#.#
            ###.#.#####.#.#
            #...#.....#.#.#
            #.#.#.###.#.#.#
            #.....#...#.#.#
            #.###.#.#.#.#.#
            #S..#.....#...#
            ###############
            """
        )
    )
    maze = Maze.parse(map)
    assert maze.get_path_length() == 7036


